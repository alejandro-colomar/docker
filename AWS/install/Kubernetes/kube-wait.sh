export debug=$debug							;
#########################################################################
su ssm-user								;
#########################################################################
set +x && test "$debug" = true && set -x				;
#########################################################################
while true								;
do									\
	test								\
		-f							\
			$HOME/.kube/config				\
	&&								\
	break								\
									;
	sleep								\
		1							\
									;
done									;	
#########################################################################
while true								;
do									\
	kubectl get node						\
	|								\
		grep Ready						\
		&&							\
		break							\
									;
	sleep								\
		1							\
									;
done									;
#########################################################################
echo ready								;
#########################################################################
